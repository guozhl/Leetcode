// @lc code=start
/**
 * @param {number[]} nums
 * @param {number} k
 * @return {void} Do not return anything, modify nums in-place instead.
 */
var rotate = function (nums, k) {
  k = k % nums.length
  let arr = nums.splice(nums.length - k)
  nums.unshift(...arr)
};
// @lc code=end
